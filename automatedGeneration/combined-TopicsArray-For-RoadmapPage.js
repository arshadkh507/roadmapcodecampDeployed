const fs = require("fs");

function combineArrays(topicPaths, fileNames) {
  const rcFrontendTopics = [];

  // loop through each item in fileNames array
  fileNames.forEach((name, index) => {
    // create an object with key of name and path
    const topic = {
      name: `<${name}/>`,
      path: topicPaths[index],
    };

    // push the object into rcFrontendTopics array
    rcFrontendTopics.push(topic);
  });

  // write rcFrontendTopics array to a file
  const content = `const rcFrontendTopics = [\n${rcFrontendTopics
    .map((topic) => `  { name: ${topic.name}, path: '${topic.path}' },`)
    .join("\n")}
];\nexport default rcFrontendTopics;`;
  fs.writeFileSync("rcAITopics.js", content);

  return rcFrontendTopics;
}

const rcFrontendTopics = combineArrays(topicPaths, fileNames);
