// Get topic names in pascal case with numbering from startingNumber
function getPascalCaseName(name, startNum = 1) {
  // Remove special characters and split by space
  const words = name.replace(/[^\w\s]/gi, "").split(" ");
  // Pascal case each word and join
  const pascalCaseName = words
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
    .join("");

  // Add starting number to pascal case name
  const paddedNum = startNum.toString().padStart(3, "0");
  const finalName = `${pascalCaseName}${paddedNum}`;

  return finalName;
}

// Get topic names
const startingNumber = 480;
const topicNames = rcCS.map((topic, index) => {
  const nameArray = topic.topicName.split("/");
  const lastPath = nameArray[nameArray.length - 1];
  return getPascalCaseName(lastPath, startingNumber + index);
});

